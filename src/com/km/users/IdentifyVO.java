package com.km.users;

public class IdentifyVO {
	private String usercode;
	private String id_pic_path;
	private String id_pic_oriname;
	private String id_pic_rename;
	public String getUsercode() {
		return usercode;
	}
	
	
	public void setUsercode(String usercode) {
		this.usercode = usercode;
	}
	public String getId_pic_path() {
		return id_pic_path;
	}
	public void setId_pic_path(String id_pic_path) {
		this.id_pic_path = id_pic_path;
	}
	public String getId_pic_oriname() {
		return id_pic_oriname;
	}
	public void setId_pic_oriname(String id_pic_oriname) {
		this.id_pic_oriname = id_pic_oriname;
	}
	public String getId_pic_rename() {
		return id_pic_rename;
	}
	public void setId_pic_rename(String id_pic_rename) {
		this.id_pic_rename = id_pic_rename;
	}
	
	

}
